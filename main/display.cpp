const char* main_menu_item[] = {"Wi-Fi", "IR", "NFC", "Signal", "Custom", "Settings"};
const char* wifi_menu_item[] = {"Back", "Scan", "Attack", "Bacon", "Still"};

int now_index                = 0;
int now_menu                 = 0;

#include "display.h"

#include <U8g2lib.h>        // lib for display.


U8G2_SH1107_SEEED_128X128_F_SW_I2C u8g2(U8G2_R0, /* clock=*/ 22, /* data=*/ 23, /* reset=*/ U8X8_PIN_NONE);       // Change to your terminal, 

Display::Display() {
    _init = 1;
}

void Display::begin() {
    Serial.begin(115200);

    u8g2.begin();
    u8g2.clearBuffer(); 
}


void Display::menu(int move){
  u8g2.clearBuffer();
  u8g2.setFont(u8g2_font_crox4t_tf);

  u8g2.drawXBMP(127, 10, 1, 112, scrol_bar);

  if (move == 49){
    now_index = now_index + 1;
  }

  if (move == 50){
    now_index = now_index - 1;
  }

  Serial.println(now_index);

  if (now_menu == 0){
    if (now_index == -1){
      now_index = 5;
    }
    if (now_index == 6){
      now_index = 0;
    }
    u8g2.drawXBMP(126, 15 + now_index * 18, 3, 7, scrol_select);
    for (int i = 0; i < 6; i++){
      if (now_index == i){
        u8g2.drawXBMP(0, 16 + 19 * i - (now_index >= 3) * 19, 116, 20, selecter);
        u8g2.drawStr(10, 32 + 19 * i - (now_index >= 3) * 19, main_menu_item[i]);
        if (move == 51){
          if (i == 0){
            now_menu = 1;
            now_index = 0;
          }
        }
      } else {
        u8g2.drawStr(3, 32 + 19 * i - (now_index >= 3) * 19, main_menu_item[i]);
      }
    }
  } else if (now_menu == 1){
    if (now_index == -1){
      now_index = 4;
    }
    if (now_index == 5){
      now_index = 0;
    }

    u8g2.drawXBMP(126, 15 + now_index * 22, 3, 7, scrol_select);

    for (int i = 0; i < 5; i++){
      if (now_index == i){
        u8g2.drawXBMP(0, 16 + 19 * i, 116, 20, selecter);
        u8g2.drawStr(10, 32 + 19 * i, wifi_menu_item[i]);
        if (move == 51){
          if (i == 0){
            now_menu = 0;
            now_index = 0;
          }
        }
      } else {
        u8g2.drawStr(3, 32 + 19 * i, wifi_menu_item[i]);
      }
    }
  }

  u8g2.drawXBMP(0, 112, 128, 16, bottom);
  u8g2.drawXBMP(0, 0, 128, 15, top);

  u8g2.sendBuffer();
}